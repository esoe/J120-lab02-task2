package ru.molokoin;

public class Math {
    /**
     * функция сложения
     * @param x
     * @param y
     * @return
     */
    public static Integer summ(Integer x, Integer y){
        return x+y;
    }
    /**
     * функция вычитания
     * @param x
     * @param y
     * @return
     */
    public static Integer subtract(Integer x, Integer y){
        return x-y;
    }
}
